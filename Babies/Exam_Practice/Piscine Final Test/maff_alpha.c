#include <unistd.h>

int	main (void)
{
	int i;
	char a; 
	char b;

	i = 0;
	a = 'a';

	while(a + i <= 'z')
	{
		b = a + i;
		if( i % 2 != 0)
		{
			b -= 32;
			write( 1, &b, 1);
		}
		else
			write( 1, &b, 1);
		i++;
	}
}
