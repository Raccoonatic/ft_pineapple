#include <unistd.h>

void ft_putstr(char *str)
{
	while(*str)
	{
		write(1, str, 1);
		str++;
	}
}

int main(int a, char *b[])
{
	(void)a;
	ft_putstr(&b[1][0]);
	return(0);
}
